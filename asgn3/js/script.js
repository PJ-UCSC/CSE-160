function setupTextures() {
  shader_vars.skyTexture = createSolidTexture(255, 255, 255, 255);
  shader_vars.groundTexture = createSolidTexture(255, 255, 255, 255);
  shader_vars.wallTexture = createMineCraftTexture();
}

function drawCube(matrix, texture, color) {
  gl.uniformMatrix4fv(shader_vars.u_ModelMatrix, false, matrix.elements);
  gl.activeTexture(gl.TEXTURE0);
  gl.bindTexture(gl.TEXTURE_2D, texture);
  gl.uniform1i(shader_vars.u_Sampler, 0);
  gl.uniform4f(shader_vars.u_Color, color[0], color[1], color[2], color[3]);
  
  gl.drawElements(gl.TRIANGLES, 36, gl.UNSIGNED_SHORT, 0);
}

function handleMouseMove(ev) {
  let x = ev.clientX;
  let y = ev.clientY;

  if (g_dragging) {
    // Calculate how much the mouse moved since the last frame
    let factor = 100 / canvas.height; // Sensitivity
    let dx = factor * (x - g_lastX);

    for(let i=0; i<Math.abs(dx); i++) {
      if(dx > 0) {
        camera.panLeft();
      } else {
        camera.panRight();
      }
    }

    // Update global angles
    //g_globalAngleY = g_globalAngleY + dx;
    //g_globalAngleX = g_globalAngleX + dy;

    // Redraw the scene with the new angles
    render();
  }

  // Update last positions
  g_lastX = x;
  g_lastY = y;
}

function render() {
  // 1. Clear the screen
  gl.enable(gl.DEPTH_TEST);
  gl.depthFunc(gl.LEQUAL);
  gl.clear(gl.COLOR_BUFFER_BIT | gl.DEPTH_BUFFER_BIT);
  gl.viewport(0, 0, canvas.width, canvas.height);

  // 2. Set Camera Matrices
  gl.uniformMatrix4fv(shader_vars.u_ProjectionMatrix, false, camera.projectionMatrix.elements);
  gl.uniformMatrix4fv(shader_vars.u_ViewMatrix, false, camera.viewMatrix.elements);

  // 3. Draw Sky Plane (viewed from inside, so culling must stay off)
  let skyMat = new Matrix4();
  skyMat.scale(1000, 1000, 1000); 
  gl.depthMask(false);
  gl.disable(gl.CULL_FACE);
  drawCube(skyMat, shader_vars.skyTexture, [0.2, 0.1, .8, 1.0]); 
  gl.depthMask(true);

  // Re-enable back-face culling so we never see the inside of solid geometry.
  gl.enable(gl.CULL_FACE);
  gl.cullFace(gl.BACK);

  // 4. Draw Ground Plane (top of cube sits at y=0 so walls aren't half-buried)
  let groundMat = new Matrix4();
  groundMat.scale(1000, 1, 1000);
  groundMat.translate(0, -0.5, 0);
  drawCube(groundMat, shader_vars.groundTexture, [0.2, 0.6, 0.2, 1.0]); 

  // 5. Draw Wall Cubes
  for (let i = 0; i < walls.length; i++) {
    let wall = walls[i];
    let wallMat = new Matrix4();
    wallMat.scale(wall.size[0], wall.size[1], wall.size[2]);
    wallMat.translate(wall.position[0], wall.position[1], wall.position[2]);
    wallMat.translate(0, 0.5, 0);
    drawCube(wallMat, shader_vars.wallTexture, wall.color);
  }
}

function setupWalls() {

  for(let y = 0; y<MAP.length; y++) {
    for(let x = 0; x<MAP[y].length; x++) {
      for(let z = 0; z<MAP[y][x].length; z++) {
        if(MAP[y][x][z] > 0) {
          const cube = new Cube();
          cube.translate(x, y, z);
          cube.scale(1, MAP[y][x][z], 1);
          walls.push(cube);
        }
      }
    }
  }
}
